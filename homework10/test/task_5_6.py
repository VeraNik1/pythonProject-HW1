# üìå–û—Å—Ç–∞–ª—å–Ω—ã–µ –∫–ª–∞—Å—Å—ã –Ω–∞—Å–ª–µ–¥—É–π—Ç–µ –æ—Ç –Ω–µ–≥–æ.
# üìå–£–±–µ–¥–∏—Ç–µ—Å—å, —á—Ç–æ –≤ —Å–æ–∑–¥–∞–Ω–Ω—ã–µ —Ä–∞–Ω–µ–µ –∫–ª–∞—Å—Å—ã –≤–Ω–µ—Å–µ–Ω—ã –ø—Ä–∞–≤–∫–∏.


class Animal:

    def __init__(self, name: str, age: int):
        self.name = name
        self.age = age
        self.voice = None

    def get_voice(self):
        return self.voice


class Dog(Animal):

    def __init__(self, name: str, age: int, voice='auuuuu'):
        super().__init__(name, age)
        self.voice = voice


class Cat(Animal):

    def __init__(self, name: str, age: int, voice='mrrr'):
        super().__init__(name, age)
        self.voice = voice


class Bird(Animal):

    def __init__(self, name: str, age: int, voice='twitwi'):
        super().__init__(name, age)
        self.voice = voice


if __name__ == '__main__':
    dog_1 = Dog('Oleg', 3, 'wooof')
    cat_1 = Cat('Fudge', 5, 'meow')
    bird_1 = Bird('Popka', 2, 'popka_khoroshiy')

    for pet in [dog_1, cat_1, bird_1]:
        print(f'{pet.name} {pet.get_voice()}')
